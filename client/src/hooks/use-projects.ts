import { useQuery } from "@tanstack/react-query";
import { api, type ProjectResponse } from "@shared/routes";

export function useProjects() {
  return useQuery({
    queryKey: [api.projects.list.path],
    queryFn: async () => {
      const res = await fetch(api.projects.list.path);
      if (!res.ok) throw new Error("Failed to fetch projects");
      return api.projects.list.responses[200].parse(await res.json());
    },
  });
}

export function useProject(id: number) {
  return useQuery({
    queryKey: [api.projects.get.path, id],
    queryFn: async () => {
      // Manually constructing URL since buildUrl helper isn't available in hooks context directly 
      // but usually imported from shared routes. Assuming standard fetch here.
      const res = await fetch(api.projects.get.path.replace(':id', String(id)));
      if (res.status === 404) return null;
      if (!res.ok) throw new Error("Failed to fetch project");
      return api.projects.get.responses[200].parse(await res.json());
    },
    enabled: !!id,
  });
}
